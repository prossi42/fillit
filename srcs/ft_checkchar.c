#include "fillit.h"

int		ft_checkchar(char *buf)
{
	int		i;
	int		j;
	int		k;

	i = -1;
	j = 0;
	k = 0;
	while (buf[++i])
	{
		if (buf[i] != '#' && buf[i] != '.' && buf[i] != '\n')
			return (1);
		if (buf[i] == '#')
			k++;
		if (buf[i] == '\n')
			j++;
		if (j == 4 && k != 4)
			return (1);
		if (j == 4)
		{
			k = 0;
			j = -1;
		}
	}
	return (0);
}
