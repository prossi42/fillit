#include "fillit.h"

int		ft_checkform(char *buf)
{
	int		i;
	int		j;
	int		k;

	i = -1;
	j = 0;
	k = 0;
	while (buf[++i])
	{
		if (buf[i] == '\n')
		{
			j++;
			if (j == 4 && (buf[i + 1] == '.' || buf[i + 1] == '#'))
				return (1);
			if (j == 4)
				j = -1;
			if (k != 4 && j != 0)
				return (1);
			if (k == 4)
				k = 0;
			if (j != 4 && buf[i + 1] == '\0')
				return (1);
		}
		if (buf[i] == '.' || buf[i] == '#')
			k++;
	}
	return (0);
}
