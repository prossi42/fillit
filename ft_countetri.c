#include "fillit.h"

int		ft_countetri(char *buf)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 0;
	k = 0;
	while (buf[i])
	{
		if (buf[i] == '\n')
			j++;
		if (j == 4)
		{
			k++;
			j = -1;
		}
		i++;
	}
	return (k);
}
